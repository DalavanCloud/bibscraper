// Look up citations in BioNames
function match_citations($citations)
{
	$references = array();
	
	
	foreach ($citations as $citation)
	{
		if (1)
		{
			echo '.';
			$references[] = lookup($citation);
		}
		else
		{
			$references[] = $citation;
		}
	}
	
	return $references;
}


