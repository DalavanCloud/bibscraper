// extract list of references from XML and return as BibJSON array
function get_references_from_xml($doi)
{
	$citations = array();
	
	$url = 'http://dx.doi.org/' . $doi;

	$html = get($url);
	
	if (preg_match('/<link rel="alternate" hreflang="en" type="application\/xml" title="XML" href="(?<xml>.*)" \/>/', $html, $m))
	{
		//print_r($m);
		
		$url = 'http://zookeys.pensoft.net' . str_replace('&amp;', '&', $m['xml']);
		
		$xml = get($url);
		
		$xp = new XsltProcessor();
		$xsl = new DomDocument;
		$xsl->load('zookeys2bibjson.xsl');
		$xp->importStylesheet($xsl);
		
		$dom = new DOMDocument;
		$dom->loadXML($xml);
		$xpath = new DOMXPath($dom);
	
		$json = $xp->transformToXML($dom);
		
		$citations = json_decode($json);
		
	}

	return $citations;
	
	
}

